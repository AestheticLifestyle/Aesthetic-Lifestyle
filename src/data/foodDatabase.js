// ── Helper: compute macros for a given food at a specific gram amount ──
export function computeMacros(food, grams) {
  const ratio = grams / 100;
  return {
    fname: food.name,
    grams: Math.round(grams),
    kcal: Math.round(food.per100.kcal * ratio),
    p: Math.round(food.per100.p * ratio),
    c: Math.round(food.per100.c * ratio),
    f: Math.round(food.per100.f * ratio),
  };
}

// ── Helper: search foods by name ──
export function searchFoods(query, category = 'All') {
  const q = query.toLowerCase().trim();
  let results = FOOD_DATABASE;
  if (category && category !== 'All') {
    results = results.filter(f => f.cat === category);
  }
  if (q) {
    results = results.filter(f => f.name.toLowerCase().includes(q));
  }
  return results;
}

